import { describe, it, expect, beforeEach, afterEach } from "vitest";
import { mkdtempSync, mkdirSync, writeFileSync, existsSync } from "node:fs";
import { tmpdir } from "node:os";
import { join, resolve } from "node:path";
import { execFileSync } from "node:child_process";
import { listProviders, getProvider, selectProvider, ProviderError, type Pass2Provider } from "../src/pass2/providers/index.js";
import { enrichWithAI } from "../src/pass2/index.js";
import { runPass1 } from "../src/pass1/index.js";

const BIN = resolve(import.meta.dirname, "../dist/bin.js");

function runCli(
  args: string[],
  cwd: string,
): { stdout: string; stderr: string; code: number } {
  try {
    const stdout = execFileSync("node", [BIN, ...args], { cwd, encoding: "utf8", stdio: ["ignore", "pipe", "pipe"] });
    return { stdout, stderr: "", code: 0 };
  } catch (err) {
    const e = err as { stdout?: Buffer | string; stderr?: Buffer | string; status?: number };
    return {
      stdout: e.stdout?.toString() ?? "",
      stderr: e.stderr?.toString() ?? "",
      code: e.status ?? 1,
    };
  }
}

function makeRepo(): string {
  const root = mkdtempSync(join(tmpdir(), "anat-p2p-"));
  writeFileSync(join(root, "package.json"), JSON.stringify({
    name: "demo", version: "1.0.0", description: "Demo lib.",
    main: "./index.js", scripts: { test: "vitest" }, engines: { node: ">=20" },
  }));
  mkdirSync(join(root, "src"));
  return root;
}

// ── Provider registry ────────────────────────────────────────────────────────

describe("provider registry", () => {
  it("listProviders returns at least claude-cli", async () => {
    const all = await listProviders();
    const names = all.map(p => p.name);
    expect(names).toContain("claude-cli");
  });

  it("getProvider finds a registered provider by name", async () => {
    expect((await getProvider("claude-cli"))?.name).toBe("claude-cli");
  });

  it("getProvider returns undefined for unknown names", async () => {
    expect(await getProvider("definitely-not-a-real-provider")).toBeUndefined();
  });

  it("selectProvider with explicit name returns it", async () => {
    const p = await selectProvider("claude-cli");
    expect(p.name).toBe("claude-cli");
  });

  it("selectProvider throws ProviderError for unknown explicit name", async () => {
    await expect(selectProvider("nope")).rejects.toThrowError(ProviderError);
    await expect(selectProvider("nope")).rejects.toThrow(/unknown provider/);
  });
});

